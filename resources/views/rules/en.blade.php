<ol>
	<li>
		The goal of the game is to predict ('pick') the top {{ config('picks.max') }} of each race as close to the actual result as possible.<br><br>
	</li>
	
	<li>
		The points system is as follows:<br>
		
		<ul>
			<li>
				1 point for each picked driver finishing in the top {{ config('picks.max') }};
				
			</li>
			<li>
				1 point for each picked driver finishing in the position you assigned.
			</li>
		</ul><br>
		
		For example, if you picked Max Verstappen for the #1 spot, you will get 2 points because he both finished in the top {{ config('picks.max') }} and finished #1.<br><br>
	</li>

	<li>
		At the end of the season, the participant with the most points (in the league) has won.<br><br>
	</li>
	
	<li>
		In case of a tie, the tiebreaker is the points total for correct finishing positions. If still tied, the points total for top 10 finishes apply.<br><br>
	</li>
	
	<li>
		Pickems can be made till the first session of the weekend for that race. This is usually Friday morning local time (Thursday at Monaco.)<br><br>
		In case the first session is delayed or cancelled, you can petition the organization to change the cutoff time. It is up to the organization to honor the request.<br><br>
	</li>
	
	<li>
		If a driver is substituted during the weekend, the picked driver will be substituted as well.<br><br>
	</li>

	<li>
		Penalties announced after the race effecting in demotions or disqualifications will only be processed
		if the penalties were announced before the start of the next race.<br><br>
	</li>

	<li>
		If you forgot to make your pickems before the cutoff, your previous pickems will carry over.
		This does not apply to the first race of the season. Already carried over pickems will also not be carried again.<br><br>
	</li>
	
	<li>
		Anything not covered by the rules on this page is up to the organization.<br><br>
	</li>
</ol>
